package oop.oop_fixing_mistakes;

public class FixingMistake {

        private String color;
        private int power;

        public FixingMistake(String color, int power) {
            this.color = color;
            this.power = power;
        }

        public String getColor() {
            return color;
        }

        public int getPower() {
            return power;
        }
}



